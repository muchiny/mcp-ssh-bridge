//! HTTP transport middleware integration tests (FIND-005).
//!
//! Verifies that the Axum router built by `build_router_with_store` carries
//! the full security middleware stack:
//!
//! - `TimeoutLayer` returns 408 for slow handlers.
//! - `RequestBodyLimitLayer` returns 413 for oversize bodies.
//! - `SetRequestIdLayer` stamps `x-request-id` on every response.
//! - `PropagateRequestIdLayer` echoes a client-supplied `x-request-id`.
//! - `SetSensitive{Request,Response}HeadersLayer` is wired (presence-only,
//!   verified via the request roundtrip succeeding — true sensitivity
//!   marking is internal to `http::Extensions` and isn't surfaced over
//!   the wire).

#![cfg(feature = "http")]

use std::sync::Arc;

use axum::body::{Body, to_bytes};
use axum::http::{Request, StatusCode};
use tower::ServiceExt;

use mcp_ssh_bridge::config::Config;
use mcp_ssh_bridge::mcp::McpServer;
use mcp_ssh_bridge::mcp::transport::http::{HttpTransportConfig, build_router};

/// Build a working router pinned to localhost defaults. Reuses production
/// `build_router` so we exercise the actual middleware stack.
fn build_test_router_with_config(http_cfg: HttpTransportConfig) -> axum::Router {
    let main_cfg = Config::default();
    let (server, _audit_task) = McpServer::new(main_cfg);
    build_router(Arc::new(server), http_cfg)
}

fn build_test_router() -> axum::Router {
    build_test_router_with_config(HttpTransportConfig::default())
}

#[tokio::test]
async fn body_over_limit_rejected() {
    // The default config caps bodies at 1 MiB (max_body_size = 1_048_576).
    // We push 2 MiB of zeroed JSON bytes so the limit layer trips.
    let huge = vec![b'a'; 2 * 1024 * 1024];

    let response = build_test_router()
        .oneshot(
            Request::builder()
                .method("POST")
                .uri("/mcp")
                .header("origin", "http://localhost:5173")
                .header("content-type", "application/json")
                .body(Body::from(huge))
                .unwrap(),
        )
        .await
        .unwrap();

    assert_eq!(
        response.status(),
        StatusCode::PAYLOAD_TOO_LARGE,
        "body over max_body_size must be rejected with 413"
    );
}

#[tokio::test]
async fn request_id_header_present_on_response() {
    // GET /health should round-trip and the response must carry an
    // x-request-id header generated by SetRequestIdLayer.
    let response = build_test_router()
        .oneshot(
            Request::builder()
                .method("GET")
                .uri("/health")
                .header("origin", "http://localhost:5173")
                .body(Body::empty())
                .unwrap(),
        )
        .await
        .unwrap();

    assert_eq!(response.status(), StatusCode::OK);
    let header = response
        .headers()
        .get("x-request-id")
        .expect("response must carry x-request-id (SetRequestIdLayer)");
    let header_str = header.to_str().expect("x-request-id is ASCII");
    // MakeRequestUuid emits a UUIDv4 string; should be at least 32 chars.
    assert!(
        header_str.len() >= 32,
        "x-request-id should look like a UUID, got {header_str:?}"
    );

    // Drain the body to avoid leaking task handles in CI.
    let _ = to_bytes(response.into_body(), 64 * 1024).await.unwrap();
}

#[tokio::test]
async fn request_id_propagated_when_supplied() {
    // PropagateRequestIdLayer must echo a client-supplied x-request-id
    // back on the response so distributed traces stay correlated.
    let supplied = "test-request-id-abc-123";

    let response = build_test_router()
        .oneshot(
            Request::builder()
                .method("GET")
                .uri("/health")
                .header("origin", "http://localhost:5173")
                .header("x-request-id", supplied)
                .body(Body::empty())
                .unwrap(),
        )
        .await
        .unwrap();

    assert_eq!(response.status(), StatusCode::OK);
    let echoed = response
        .headers()
        .get("x-request-id")
        .expect("response must carry x-request-id")
        .to_str()
        .unwrap();
    assert_eq!(
        echoed, supplied,
        "PropagateRequestIdLayer must echo client-supplied x-request-id"
    );

    let _ = to_bytes(response.into_body(), 64 * 1024).await.unwrap();
}

